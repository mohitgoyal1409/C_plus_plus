#include<iostream>
using namespace std;
int main() {
    int n;
    cout<<"n=";
    cin>>n;
    for(int i=1;i<=n;i++) {
        for(int j=1;j<=n-i+1;j++) {
            cout<<j;
        }
        for(int j=1;j<=2*i-2;j++) {
            // if(i>1)
            cout<<"*";
        }
        for(int k=n+1-i;k>=1;k--) {
            cout<<k;
        }
        cout<<endl;
    
    }

    return 0;
}